    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        AddCart cart = new AddCart();
        OrderDao OrderDao = new OrderDao();
        CustomerDao dao = new CustomerDao();
        int id = Integer.parseInt(request.getParameter("id"));

        Cart c = cart.getCartById(id);
        int Cid = c.getCid();
        cart.deleteCartDetailbycid(Cid);
        cart.deleteCartbyuid(id);
        
        ArrayList<Integer> listOd = OrderDao.listOrderIDbyCID(id);
        if (!listOd.isEmpty()) {
            for (Integer integer : listOd) {
                OrderDao.deleteOrderDetailbyoid(integer);
            }
            OrderDao.deleteOrderbyuid(id);
        }

        dao.deleteCustomer(id);
        response.sendRedirect("admin_user");

        
    }

